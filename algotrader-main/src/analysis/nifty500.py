def get_sector(symbol: str) -> str:
    """Get sector for a symbol. Falls back to 'Other'."""
    return NIFTY_500_SECTOR_MAP.get(symbol, "Other")


def get_universe() -> list[str]:
    """Get full NIFTY 500 universe."""
    return NIFTY_500_SYMBOLS.copy()


def get_sectors() -> dict[str, list[str]]:
    """Get sector → symbols mapping."""
    sectors: dict[str, list[str]] = {}
    for sym in NIFTY_500_SYMBOLS:
        sector = NIFTY_500_SECTOR_MAP.get(sym, "Other")
        sectors.setdefault(sector, []).append(sym)
    return sectors
